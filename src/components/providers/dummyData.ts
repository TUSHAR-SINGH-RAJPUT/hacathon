const generateReviews = (providerName: string, baseRating: number): Review[] => {
  const reviews: Review[] = [];
  const reviewCount = Math.floor(Math.random() * 8) + 3; // 3 to 10 reviews
  const names = ["Aarav S.", "Isha K.", "Vihaan M.", "Myra D.", "Advik P.", "Anaya R.", "Rohan J.", "Saanvi T."];
  const comments = [
    `Excellent work by ${providerName}! Very professional and punctual.`,
    `${providerName} did a fantastic job. Highly recommended!`,
    `Very satisfied with ${providerName}'s service. Will hire again.`,
    `Good communication and quality work from ${providerName}.`,
    `A reliable professional. ${providerName} understood my requirements well.`,
    `The project was completed on time and within budget by ${providerName}.`,
    `Impressed with the attention to detail shown by ${providerName}.`,
  ];

  for (let i = 0; i < reviewCount; i++) {
    reviews.push({
      id: `${providerName.replace(/\s+/g, '-')}-review-${i + Date.now()}`,
      reviewerName: names[Math.floor(Math.random() * names.length)],
      rating: Math.min(5, Math.max(3, baseRating - 1 + Math.random() * 2)), // Rating around baseRating, min 3
      comment: comments[Math.floor(Math.random() * comments.length)],
      date: new Date(Date.now() - Math.floor(Math.random() * 90) * 24 * 60 * 60 * 1000).toISOString(),
    });
  }
  return reviews;
};


